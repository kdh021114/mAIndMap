function beginPan(event) {
  if (event.button !== 0) return;
  if (event.target.closest?.('.node-card, .edge-label')) return;
  panGesture = {
    pointerId: event.pointerId,
    startClientX: event.clientX,
    startClientY: event.clientY,
    startViewX: graphView.x,
    startViewY: graphView.y,
    moved: false,
  };
  el.graphViewport.classList.add('panning');
  el.graphViewport.setPointerCapture(event.pointerId);
}

function updatePan(event) {
  if (!panGesture || event.pointerId !== panGesture.pointerId) return;
  if (Math.hypot(event.clientX - panGesture.startClientX, event.clientY - panGesture.startClientY) > GRAPH_VIEW.dragThreshold) {
    panGesture.moved = true;
  }
  graphView.x = panGesture.startViewX + event.clientX - panGesture.startClientX;
  graphView.y = panGesture.startViewY + event.clientY - panGesture.startClientY;
  applyGraphView();
}

function endPan(event) {
  if (!panGesture || event.pointerId !== panGesture.pointerId) return;
  const wasClick = !panGesture.moved;
  panGesture = null;
  el.graphViewport.classList.remove('panning');
  if (el.graphViewport.hasPointerCapture(event.pointerId)) {
    el.graphViewport.releasePointerCapture(event.pointerId);
  }
  if (wasClick) {
    if (state && (!state.nodes || state.nodes.length === 0)) {
      createRoot();
      return;
    }
    clearNodeSelectionAndCollapseChat();
  }
}

function handleGraphWheel(event) {
  event.preventDefault();
  const point = viewportPoint(event);
  if (event.ctrlKey || event.metaKey || event.altKey) {
    const multiplier = Math.exp(-event.deltaY * GRAPH_VIEW.wheelZoomSpeed);
    zoomAtViewportPoint(graphView.scale * multiplier, point);
    return;
  }

  graphView.x -= event.deltaX;
  graphView.y -= event.deltaY;
  applyGraphView();
}

function beginNodeDrag(event, node, card) {
  if (event.button !== 0) return;
  if (event.target.closest?.('.node-actions, .node-title-input')) return;
  event.preventDefault();
  event.stopPropagation();
  const dragItems = selectedNodeIds.has(node.id) && selectedNodeIds.size > 1
    ? [...selectedNodeIds].map((nodeId) => nodeDragItem(nodeId)).filter(Boolean)
    : [nodeDragItem(node.id, card)].filter(Boolean);
  nodeDrag = {
    pointerId: event.pointerId,
    nodeId: node.id,
    card,
    items: dragItems,
    startClientX: event.clientX,
    startClientY: event.clientY,
    startX: node.layout.x,
    startY: node.layout.y,
    lastSnapX: node.layout.x,
    lastSnapY: node.layout.y,
    multiSelect: selectionMode || event.shiftKey,
    moved: false,
  };
  for (const item of dragItems) {
    item.card?.classList.add('dragging');
  }
  setDraggingAffordance(true);
  card.setPointerCapture(event.pointerId);
}

function nodeDragItem(nodeId, fallbackCard = null) {
  const node = state.nodes.find((item) => item.id === nodeId);
  if (!node) return null;
  return {
    nodeId,
    card: fallbackCard || el.nodeLayer.querySelector(`[data-node-id="${nodeId}"]`),
    startX: node.layout.x,
    startY: node.layout.y,
  };
}

function updateNodeDrag(event) {
  if (!nodeDrag || event.pointerId !== nodeDrag.pointerId) return;
  const dx = (event.clientX - nodeDrag.startClientX) / graphView.scale;
  const dy = (event.clientY - nodeDrag.startClientY) / graphView.scale;
  if (Math.hypot(event.clientX - nodeDrag.startClientX, event.clientY - nodeDrag.startClientY) > GRAPH_VIEW.dragThreshold) {
    nodeDrag.moved = true;
  }

  const snappedPrimary = snapToGrid({
    x: nodeDrag.startX + dx,
    y: nodeDrag.startY + dy,
  });
  const snapDelta = {
    x: snappedPrimary.x - nodeDrag.startX,
    y: snappedPrimary.y - nodeDrag.startY,
  };
  if (nodeDrag.lastSnapX !== snappedPrimary.x || nodeDrag.lastSnapY !== snappedPrimary.y) {
    pulseSnapFeedback();
    nodeDrag.lastSnapX = snappedPrimary.x;
    nodeDrag.lastSnapY = snappedPrimary.y;
  }

  for (const item of nodeDrag.items) {
    const next = {
      x: item.startX + snapDelta.x,
      y: item.startY + snapDelta.y,
    };
    moveNodeLocally(item.nodeId, next);
    if (item.card) {
      item.card.style.left = `${next.x}px`;
      item.card.style.top = `${next.y}px`;
    }
  }
  redrawEdges();
}

async function endNodeDrag(event) {
  if (!nodeDrag || event.pointerId !== nodeDrag.pointerId) return;
  const drag = nodeDrag;
  nodeDrag = null;
  setDraggingAffordance(false);
  for (const item of drag.items) {
    item.card?.classList.remove('dragging');
  }
  if (drag.card.hasPointerCapture(event.pointerId)) {
    drag.card.releasePointerCapture(event.pointerId);
  }

  if (!drag.moved) {
    try {
      if (drag.multiSelect) {
        toggleNodeMultiSelection(drag.nodeId);
      } else {
        resetMultiSelection();
        await selectNode(drag.nodeId);
      }
    } catch (error) {
      showError(error);
    }
    return;
  }

  const positions = drag.items
    .map((item) => state.nodes.find((node) => node.id === item.nodeId))
    .filter(Boolean)
    .map((node) => ({
      nodeId: node.id,
      x: node.layout.x,
      y: node.layout.y,
    }));
  if (positions.length === 0) return;
  try {
    const beforeSnapshot = await workspaceSnapshot();
    const beforeUiState = captureUiState();
    const data = await api('/api/nodes/positions', {
      method: 'PATCH',
      body: JSON.stringify({ positions }),
    });
    state = data.state;
    await pushHistoryEntry(beforeSnapshot, beforeUiState);
    renderAll();
  } catch (error) {
    showError(error);
    loadState().catch(showError);
  }
}

function moveNodeLocally(nodeId, position) {
  const node = state.nodes.find((item) => item.id === nodeId);
  if (!node) return;
  node.layout = { ...node.layout, x: position.x, y: position.y };
  node.position = { x: position.x, y: position.y };
}

function snapToGrid(position) {
  const gridSize = GRAPH_VIEW.gridSize;
  return {
    x: Math.round(position.x / gridSize) * gridSize,
    y: Math.round(position.y / gridSize) * gridSize,
  };
}

function pulseSnapFeedback() {
  if (!navigator.vibrate) return;
  navigator.vibrate(4);
}

function redrawEdges() {
  el.edgeLayer.innerHTML = '';
  el.edgeLabelLayer.innerHTML = '';
  const contextHighlight = selectedContextHighlight();
  const layoutById = new Map(state.nodes.map((node) => [node.id, node.layout]));

  // Recompute phrase frequency table so duplicate dimming stays consistent
  // when edges are redrawn outside of a full renderGraph() pass.
  edgePhraseCounts = new Map();
  edgePhraseFirstSeen = new Map();
  for (const edge of state.edges) {
    if (isEdgeLabelImportant(edge)) continue;
    const key = normalizeEdgePhraseKey(edge.displayPhrase);
    if (!key) continue;
    edgePhraseCounts.set(key, (edgePhraseCounts.get(key) || 0) + 1);
    if (!edgePhraseFirstSeen.has(key)) {
      edgePhraseFirstSeen.set(key, edge.id);
    }
  }

  for (const edge of state.edges) {
    const source = layoutById.get(edge.sourceNodeId);
    const target = layoutById.get(edge.targetNodeId);
    if (!source || !target) continue;
    drawEdge(edge, source, target, contextHighlight);
  }
}

function clamp(value, min, max) {
  return Math.min(Math.max(value, min), max);
}

function applyDepthColors(element, depth, isEdge = false) {
  const maxDepth = Math.max(0, ...state.nodes.map((node) => node.depth || 0));
  const ratio = maxDepth === 0 ? 0 : clamp(depth / maxDepth, 0, 1);
  const eased = ratio ** 0.78;
  const hue = (258 - depth * 34 + maxDepth * 5 + 360) % 360;
  const saturation = lerp(76, 46, eased);
  const lightness = lerp(88, 94, eased);
  const secondLightness = Math.min(98, lightness + 4);
  const borderLightness = lerp(80, 88, eased);
  const accentLightness = lerp(58, 64, eased);
  const accentSaturation = lerp(68, 48, eased);

  if (isEdge) {
    element.style.stroke = `hsl(${hue} ${Math.max(34, saturation - 14)}% ${Math.min(76, borderLightness - 5)}%)`;
    return;
  }
  element.style.setProperty('--depth-accent', `hsl(${hue} ${accentSaturation}% ${accentLightness}%)`);
  element.style.setProperty('--depth-border', `hsl(${hue} ${Math.max(18, saturation - 8)}% ${borderLightness}%)`);
  element.style.setProperty('--depth-text', '#111827');
  element.style.setProperty('--depth-muted', '#6b7280');
  element.style.setProperty(
    '--depth-fill',
    `linear-gradient(135deg, hsl(${hue} ${saturation}% ${lightness}%), hsl(${hue + 16} ${Math.max(34, saturation - 8)}% ${secondLightness}%))`,
  );
}

function lerp(start, end, amount) {
  return start + (end - start) * amount;
}

