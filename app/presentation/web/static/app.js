function edgeAnchors(source, target) {
  const sourceCenter = centerOf(source);
  const targetCenter = centerOf(target);
  const dx = targetCenter.x - sourceCenter.x;
  const dy = targetCenter.y - sourceCenter.y;

  if (Math.abs(dx) > Math.abs(dy)) {
    const sign = dx >= 0 ? 1 : -1;
    return {
      sx: sign > 0 ? source.x + source.width : source.x,
      sy: sourceCenter.y,
      tx: sign > 0 ? target.x : target.x + target.width,
      ty: targetCenter.y,
      axis: 'x',
      sign,
    };
  }

  const sign = dy >= 0 ? 1 : -1;
  return {
    sx: sourceCenter.x,
    sy: sign > 0 ? source.y + source.height : source.y,
    tx: targetCenter.x,
    ty: sign > 0 ? target.y : target.y + target.height,
    axis: 'y',
    sign,
  };
}

function centerOf(box) {
  return {
    x: box.x + box.width / 2,
    y: box.y + box.height / 2,
  };
}

function applyGraphView() {
  el.graphCanvas.style.setProperty('--graph-pan-x', `${graphView.x}px`);
  el.graphCanvas.style.setProperty('--graph-pan-y', `${graphView.y}px`);
  el.graphCanvas.style.setProperty('--graph-scale', graphView.scale);
  el.graphViewport.style.setProperty('--graph-pan-x', `${graphView.x}px`);
  el.graphViewport.style.setProperty('--graph-pan-y', `${graphView.y}px`);
  el.graphViewport.style.setProperty('--graph-scale', graphView.scale);
}

function centerViewOnGraph() {
  if (!state?.nodes.length) {
    centerViewOnPoint(0, 0);
    return;
  }

  const bounds = graphBounds();
  const rect = el.graphViewport.getBoundingClientRect();
  const scaleX = rect.width / Math.max(bounds.width + 180, 1);
  const scaleY = rect.height / Math.max(bounds.height + 180, 1);
  graphView.scale = clamp(Math.min(1, scaleX, scaleY), GRAPH_VIEW.minScale, GRAPH_VIEW.maxScale);
  graphView.x = rect.width / 2 - (bounds.x + bounds.width / 2) * graphView.scale;
  graphView.y = rect.height / 2 - (bounds.y + bounds.height / 2) * graphView.scale;
}

function centerViewOnNode(nodeId) {
  const node = state?.nodes.find((item) => item.id === nodeId);
  if (!node) {
    centerViewOnGraph();
    return;
  }
  const center = centerOf(node.layout);
  centerViewOnPoint(center.x, center.y);
}

function centerViewOnPoint(worldX, worldY) {
  const rect = el.graphViewport.getBoundingClientRect();
  graphView.x = rect.width / 2 - worldX * graphView.scale;
  graphView.y = rect.height / 2 - worldY * graphView.scale;
}

function graphBounds() {
  const boxes = state.nodes.map((node) => node.layout);
  const minX = Math.min(...boxes.map((box) => box.x));
  const minY = Math.min(...boxes.map((box) => box.y));
  const maxX = Math.max(...boxes.map((box) => box.x + box.width));
  const maxY = Math.max(...boxes.map((box) => box.y + box.height));
  return {
    x: minX,
    y: minY,
    width: maxX - minX,
    height: maxY - minY,
  };
}

function viewportPoint(event) {
  const rect = el.graphViewport.getBoundingClientRect();
  return {
    x: event.clientX - rect.left,
    y: event.clientY - rect.top,
  };
}

function zoomAtViewportPoint(nextScale, point) {
  const clampedScale = clamp(nextScale, GRAPH_VIEW.minScale, GRAPH_VIEW.maxScale);
  const worldX = (point.x - graphView.x) / graphView.scale;
  const worldY = (point.y - graphView.y) / graphView.scale;
  graphView.x = point.x - worldX * clampedScale;
  graphView.y = point.y - worldY * clampedScale;
  graphView.scale = clampedScale;
  applyGraphView();
}

function zoomFromCenter(multiplier) {
  const rect = el.graphViewport.getBoundingClientRect();
  zoomAtViewportPoint(graphView.scale * multiplier, {
    x: rect.width / 2,
    y: rect.height / 2,
  });
}

function beginPan(event) {
  if (event.button !== 0) return;
  if (event.target.closest?.('.node-card, .edge-label')) return;
  panGesture = {
    pointerId: event.pointerId,
    startClientX: event.clientX,
    startClientY: event.clientY,
    startViewX: graphView.x,
    startViewY: graphView.y,
  };
  el.graphViewport.classList.add('panning');
  el.graphViewport.setPointerCapture(event.pointerId);
}

function updatePan(event) {
  if (!panGesture || event.pointerId !== panGesture.pointerId) return;
  graphView.x = panGesture.startViewX + event.clientX - panGesture.startClientX;
  graphView.y = panGesture.startViewY + event.clientY - panGesture.startClientY;
  applyGraphView();
}

function endPan(event) {
  if (!panGesture || event.pointerId !== panGesture.pointerId) return;
  panGesture = null;
  el.graphViewport.classList.remove('panning');
  if (el.graphViewport.hasPointerCapture(event.pointerId)) {
    el.graphViewport.releasePointerCapture(event.pointerId);
  }
}

function handleGraphWheel(event) {
  event.preventDefault();
  const point = viewportPoint(event);
  if (event.ctrlKey || event.metaKey || event.altKey) {
    const multiplier = Math.exp(-event.deltaY * GRAPH_VIEW.wheelZoomSpeed);
    zoomAtViewportPoint(graphView.scale * multiplier, point);
    return;
  }

  graphView.x -= event.deltaX;
  graphView.y -= event.deltaY;
  applyGraphView();
}

function beginNodeDrag(event, node, card) {
  if (event.button !== 0) return;
  if (event.target.closest?.('.node-title, .node-actions, .node-title-input')) return;
  event.preventDefault();
  event.stopPropagation();
  nodeDrag = {
    pointerId: event.pointerId,
    nodeId: node.id,
    card,
    startClientX: event.clientX,
    startClientY: event.clientY,
    startX: node.layout.x,
    startY: node.layout.y,
    moved: false,
  };
  card.classList.add('dragging');
  card.setPointerCapture(event.pointerId);
}

function updateNodeDrag(event) {
  if (!nodeDrag || event.pointerId !== nodeDrag.pointerId) return;
  const dx = (event.clientX - nodeDrag.startClientX) / graphView.scale;
  const dy = (event.clientY - nodeDrag.startClientY) / graphView.scale;
  if (Math.hypot(event.clientX - nodeDrag.startClientX, event.clientY - nodeDrag.startClientY) > GRAPH_VIEW.dragThreshold) {
    nodeDrag.moved = true;
  }

  const next = {
    x: nodeDrag.startX + dx,
    y: nodeDrag.startY + dy,
  };
  moveNodeLocally(nodeDrag.nodeId, next);
  nodeDrag.card.style.left = `${next.x}px`;
  nodeDrag.card.style.top = `${next.y}px`;
  redrawEdges();
}

async function endNodeDrag(event) {
  if (!nodeDrag || event.pointerId !== nodeDrag.pointerId) return;
  const drag = nodeDrag;
  nodeDrag = null;
  drag.card.classList.remove('dragging');
  if (drag.card.hasPointerCapture(event.pointerId)) {
    drag.card.releasePointerCapture(event.pointerId);
  }

  if (!drag.moved) {
    try {
      await selectNode(drag.nodeId);
    } catch (error) {
      showError(error);
    }
    return;
  }

  const node = state.nodes.find((item) => item.id === drag.nodeId);
  if (!node) return;
  try {
    const data = await api(`/api/nodes/${drag.nodeId}/position`, {
      method: 'PATCH',
      body: JSON.stringify({ position: { x: node.layout.x, y: node.layout.y } }),
    });
    state = data.state;
    renderAll();
  } catch (error) {
    showError(error);
    loadState().catch(showError);
  }
}

function moveNodeLocally(nodeId, position) {
  const node = state.nodes.find((item) => item.id === nodeId);
  if (!node) return;
  node.layout = { ...node.layout, x: position.x, y: position.y };
  node.position = { x: position.x, y: position.y };
}

function redrawEdges() {
  el.edgeLayer.innerHTML = '';
  el.edgeLabelLayer.innerHTML = '';
  const layoutById = new Map(state.nodes.map((node) => [node.id, node.layout]));
  for (const edge of state.edges) {
    const source = layoutById.get(edge.sourceNodeId);
    const target = layoutById.get(edge.targetNodeId);
    if (!source || !target) continue;
    drawEdge(edge, source, target);
  }
}

function clamp(value, min, max) {
  return Math.min(Math.max(value, min), max);
}

function applyDepthColors(element, depth, isEdge = false) {
  const maxDepth = Math.max(0, ...state.nodes.map((node) => node.depth || 0));
  const ratio = maxDepth === 0 ? 0 : clamp(depth / maxDepth, 0, 1);
  const eased = ratio ** 0.86;
  const hue = lerp(224, 204, eased);
  const saturation = lerp(76, 18, eased);
  const lightness = lerp(91, 99, eased);
  const secondLightness = Math.min(99, lightness + lerp(4, 0, eased));
  const borderLightness = lerp(78, 92, eased);
  const accentLightness = lerp(56, 66, eased);
  const accentSaturation = lerp(72, 34, eased);

  if (isEdge) {
    element.style.stroke = `hsl(${hue} ${Math.max(28, saturation)}% ${Math.min(84, borderLightness)}%)`;
    return;
  }
  element.style.setProperty('--depth-accent', `hsl(${hue} ${accentSaturation}% ${accentLightness}%)`);
  element.style.setProperty('--depth-border', `hsl(${hue} ${Math.max(18, saturation - 8)}% ${borderLightness}%)`);
  element.style.setProperty('--depth-text', '#111827');
  element.style.setProperty('--depth-muted', '#6b7280');
  element.style.setProperty(
    '--depth-fill',
    `linear-gradient(135deg, hsl(${hue} ${saturation}% ${lightness}%), hsl(${hue + 8} ${Math.max(24, saturation - 10)}% ${secondLightness}%))`,
  );
}

function lerp(start, end, amount) {
  return start + (end - start) * amount;
}

function startNodeTitleEdit(node, titleElement) {
  if (titleElement.querySelector('input')) return;

  const input = document.createElement('input');
  input.className = 'node-title-input';
  input.value = node.displayTitle;
  input.setAttribute('aria-label', t('editNodeTitle'));
  titleElement.classList.add('editing');
  titleElement.replaceChildren(input);
  input.focus();
  input.select();

  let finished = false;
  const finish = async (shouldSave) => {
    if (finished) return;
    finished = true;
    const nextTitle = input.value.trim();
    titleElement.classList.remove('editing');

    if (!shouldSave || !nextTitle || nextTitle === node.displayTitle) {
      titleElement.textContent = node.displayTitle;
      return;
    }

    try {
      const data = await api(`/api/nodes/${node.id}/title`, {
        method: 'PATCH',
        body: JSON.stringify({ title: nextTitle }),
      });
      state = data.state;
      renderAll();
    } catch (error) {
      showError(error);
      titleElement.textContent = node.displayTitle;
    }
  };

  input.addEventListener('pointerdown', (event) => event.stopPropagation());
  input.addEventListener('keydown', (event) => {
    event.stopPropagation();
    if (event.key === 'Enter') {
      event.preventDefault();
      finish(true);
    }
    if (event.key === 'Escape') {
      event.preventDefault();
      finish(false);
    }
  });
  input.addEventListener('blur', () => finish(true));
}

function startEdgePhraseEdit(edge, labelElement) {
  if (labelElement.querySelector('input')) return;

  const input = document.createElement('input');
  input.className = 'edge-label-input';
  input.value = edge.displayPhrase || '';
  input.setAttribute('aria-label', t('editRelation'));
  labelElement.classList.add('editing');
  labelElement.replaceChildren(input);
  input.focus();
  input.select();

  let finished = false;
  const finish = async (shouldSave) => {
    if (finished) return;
    finished = true;
    const nextPhrase = input.value.trim();
    labelElement.classList.remove('editing');

    if (!shouldSave || !nextPhrase || nextPhrase === (edge.displayPhrase || '')) {
      labelElement.textContent = edge.displayPhrase || '...';
      return;
    }

    try {
      const data = await api(`/api/edges/${edge.id}/phrase`, {
        method: 'PATCH',
        body: JSON.stringify({ phrase: nextPhrase }),
      });
      state = data.state;
      renderAll();
    } catch (error) {
      showError(error);
      labelElement.textContent = edge.displayPhrase || '...';
    }
  };

  input.addEventListener('pointerdown', (event) => event.stopPropagation());
  input.addEventListener('keydown', (event) => {
    event.stopPropagation();
    if (event.key === 'Enter') {
      event.preventDefault();
      finish(true);
    }
    if (event.key === 'Escape') {
      event.preventDefault();
      finish(false);
    }
  });
  input.addEventListener('blur', () => finish(true));
}

