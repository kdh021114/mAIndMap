function endSidebarResize(event) {
  if (!sidebarResize || event.pointerId !== sidebarResize.pointerId) return;
  sidebarResize = null;
  el.chatSidebar.classList.remove('resizing');
  if (el.chatResizeHandle.hasPointerCapture(event.pointerId)) {
    el.chatResizeHandle.releasePointerCapture(event.pointerId);
  }
}

