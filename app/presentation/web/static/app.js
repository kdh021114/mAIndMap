async function confirmSplit() {
  if (!selectedNodeId) return;
  if (splitSelectedMessageIds.size === 0) {
    showError({ message: t('splitNeedOne') });
    return;
  }
  if (splitSelectedMessageIds.size >= messages.length) {
    showError({ message: t('splitCannotMoveAll') });
    return;
  }
  const messageIds = messages
    .filter((message) => splitSelectedMessageIds.has(message.id))
    .map((message) => message.id);
  try {
    const sourceNodeId = selectedNodeId;
    const data = await runUndoable(async () => api(`/api/nodes/${sourceNodeId}/split`, {
      method: 'POST',
      body: JSON.stringify({ messageIds }),
    }));
    state = data.state;
    splitMode = false;
    splitSelectedMessageIds = new Set();
    const newNodeId = data.result?.newNode?.id;
    if (newNodeId) {
      selectedNodeId = newNodeId;
      pendingCenterNodeId = newNodeId;
    }
    await reloadSelectedMessages();
    renderAll();
  } catch (error) {
    showError(error);
  }
}

function handleGlobalKeydown(event) {
  if (isUndoRedoShortcut(event)) {
    if (shouldUseNativeTextUndo(event)) return;
    event.preventDefault();
    if (event.shiftKey) {
      redoLastAction();
    } else {
      undoLastAction();
    }
    return;
  }

  if (isTextEditingTarget(event.target)) return;
  if (event.key === 'Escape') {
    if (selectedNodeId || selectedNodeIds.size > 0 || selectionMode || !chatSidebarHidden) {
      event.preventDefault();
      clearNodeSelectionAndCollapseChat();
    }
    return;
  }

  if ((event.key === 'Delete' || event.key === 'Backspace') && selectedNodeIds.size > 0) {
    event.preventDefault();
    deleteSelectedNodes();
  }
}

function isUndoRedoShortcut(event) {
  return (event.metaKey || event.ctrlKey) && event.key.toLowerCase() === 'z';
}

function shouldUseNativeTextUndo(event) {
  if (!isTextEditingTarget(event.target)) return false;
  return !(event.target === el.messageInput && el.messageInput.value.length === 0);
}

function isTextEditingTarget(target) {
  return Boolean(target?.closest?.('input, textarea, select, [contenteditable="true"]'));
}

