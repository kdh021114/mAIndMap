async function sendMessage(event) {
  event.preventDefault();
  const node = getSelectedNode();
  if (!node || isSending) return;
  const content = el.messageInput.value.trim();
  if (!content) return;
  const useWebSearch = webSearchEnabled && Boolean(state.webSearchAvailable);

  try {
    const beforeSnapshot = await workspaceSnapshot();
    const beforeUiState = captureUiState();
    isSending = true;
    messages = [
      ...messages,
      {
        id: `local_${Date.now()}`,
        thread_id: node.threadId,
        role: 'user',
        content,
        created_at: new Date().toISOString(),
      },
    ];
    el.messageInput.value = '';
    renderAll();

    const data = await api(`/api/nodes/${node.id}/messages`, {
      method: 'POST',
      body: JSON.stringify({ content, webSearchEnabled: useWebSearch }),
    });
    state = data.state;
    messages = data.messages;
    selectedNodeId = node.id;
    nodeMessagesCache.set(node.id, Array.isArray(messages) ? [...messages] : []);
    await pushHistoryEntry(beforeSnapshot, beforeUiState);
  } catch (error) {
    showError(error);
  } finally {
    isSending = false;
    renderAll();
  }
}

