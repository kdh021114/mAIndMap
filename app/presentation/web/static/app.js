function showDeleteConfirmation(node) {
  dismissDeleteConfirmation();

  const popover = document.createElement('div');
  popover.className = 'delete-confirm-popover';
  popover.style.left = `${node.layout.x + node.layout.width + 20}px`;
  popover.style.top = `${node.layout.y + node.layout.height / 2}px`;
  popover.addEventListener('pointerdown', (event) => event.stopPropagation());

  const title = document.createElement('div');
  title.className = 'delete-confirm-title';
  title.textContent = t('deleteConfirmTitle');

  const body = document.createElement('div');
  body.className = 'delete-confirm-body';
  body.textContent = t('deleteConfirm');

  const actions = document.createElement('div');
  actions.className = 'delete-confirm-actions';

  const cancelButton = document.createElement('button');
  cancelButton.className = 'delete-confirm-button cancel';
  cancelButton.type = 'button';
  cancelButton.textContent = t('cancel');
  cancelButton.addEventListener('click', dismissDeleteConfirmation);

  const deleteButton = document.createElement('button');
  deleteButton.className = 'delete-confirm-button delete';
  deleteButton.type = 'button';
  deleteButton.textContent = t('confirmDelete');
  deleteButton.addEventListener('click', () => performDeleteNodeById(node.id));

  actions.appendChild(cancelButton);
  actions.appendChild(deleteButton);
  popover.appendChild(title);
  popover.appendChild(body);
  popover.appendChild(actions);
  el.nodeLayer.appendChild(popover);
  deleteConfirmEl = popover;
}

function dismissDeleteConfirmation() {
  if (!deleteConfirmEl) return;
  deleteConfirmEl.remove();
  deleteConfirmEl = null;
}

async function performDeleteNodeById(nodeId) {
  try {
    dismissDeleteConfirmation();
    state = await api(`/api/nodes/${nodeId}`, { method: 'DELETE' });
    if (!selectedNodeId || !state.nodes.some((node) => node.id === selectedNodeId)) {
      selectedNodeId = null;
      messages = [];
    }
    renderAll();
  } catch (error) {
    showError(error);
  }
}

